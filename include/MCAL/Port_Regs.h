/**
 * @brief Base address of DDRA.
 */
#define DDR_BASE    (0x3A)

/**
 * @brief DDR offset between consecutive DDR.
 */
#define DDR_OFFSET  (-3)

/**
 * @brief       Calculate DDR address.
 * @param [in]  port: Port name.
 */
#define DDR(port) (DDR_BASE + (DDR_OFFSET * port))

